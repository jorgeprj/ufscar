package br.ufscar.dc.dsw.bean;

public class Conversor {

    private double min;
    private double max;
    private double inc;

    public Conversor() {
    }

    public double getMin() {
        return this.min;
    }

    public void setMin(double min) {
        this.min = min;
    }

    public double getMax() {
        return this.max;
    }

    public void setMax(double max) {
        this.max = max;
    }

    public double getInc() {
        return this.inc;
    }

    public void setInc(double inc) {
        this.max = inc;
    }
}
